    public void ItemCost()
    {
        double suma = 0;
        for(int i = 0; i < jTable1.getRowCount(); i++)
        {
            suma =  suma + Double.parseDouble(jTable1.getValueAt(i, 2).toString());
          
        }
        //jtxtSubTotal.setText(Integer.toString(sum));
        jtxtPret_brut.setText(Double.toString(suma));
        double cTotal1 = Double.parseDouble(jtxtPret_brut.getText());
        
        double TVA = (cTotal1 *19)/100;
        String iTaxTotal = String.format("%.2f LEI", TVA);
        jtxtTVA.setText(iTaxTotal);
        
        String iSubTotal = String.format("%.2f LEI", cTotal1);
        jtxtPret_brut.setText(iSubTotal);
        
        String iTotal = String.format("%.2f LEI", TVA + cTotal1);
        jtxtPret_Total.setText(iTotal); 
        
       
    }
//==========================================Function================================================
     public void calcul_rest()
    {        
        double suma= 0;
        double TVA =19;        
        double cash = Double.parseDouble(jtxtNumerar.getText());
        for(int i = 0; i < jTable1.getRowCount(); i++)
        {
            suma =  suma + Double.parseDouble(jTable1.getValueAt(i, 2).toString());            
        }
        double TVA1 = (suma * TVA)/100; 
        double act = (cash - (suma + TVA1));      
        String actualizare = String.format(" %.2f LEI", act);
        jtxtRest.setText(actualizare);     
  
}
    
