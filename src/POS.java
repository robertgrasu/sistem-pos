            {
                calcul_rest();               
            }
        
        String numar_introdus = jtxtNumerar.getText();
                       
        if (numar_introdus == "" && jtxtMetoda_plata.getSelectedItem().equals("Numerar"))
        {      
            
                jtxtRest.setText("");
                jtxtNumerar.setText("");
            }
